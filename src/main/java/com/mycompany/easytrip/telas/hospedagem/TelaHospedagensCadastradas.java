    public int receberHospedagemSelecionada(){
        int linha = hospedagemTable.getSelectedRow();
        int hospedagemId = 0;

        if(linha != -1)
            hospedagemId = (Integer)hospedagemTable.getValueAt(linha, 0);
        
        return hospedagemId;
    }
    
